function getFilename(url) {
  return url.split('/').pop()
}

function toPublicId(url) {
  const m = url.match(/\/upload\/v\d+\/(.+)$/)
  if (!m) return null
  return m[1].replace(/\.[^.]+$/, '')
}

async function main() {
  const toDelete = [...new Set(ALL_SCANNED.filter(url => !EXEMPT_FILENAMES.has(getFilename(url))))]
  const exempted = ALL_SCANNED.filter(url => EXEMPT_FILENAMES.has(getFilename(url)))
  console.log(`Total scanned: ${ALL_SCANNED.length}`)
  console.log(`Exempted (kept): ${exempted.length} — ${exempted.map(getFilename).join(', ')}`)
  console.log(`To delete: ${toDelete.length}`)

  const publicIds = toDelete.map(toPublicId).filter(Boolean)

  const pool = new Pool({ connectionString: DB_URL, ssl: { rejectUnauthorized: false } })
  try {
    const res = await pool.query(`SELECT id, images FROM terminals WHERE images && $1::text[]`, [toDelete])
    console.log(`Found ${res.rows.length} terminals with flagged images`)
    let updated = 0
    for (const row of res.rows) {
      const cleaned = (row.images || []).filter(img => !toDelete.includes(img))
      if (cleaned.length !== (row.images || []).length) {
        await pool.query(`UPDATE terminals SET images = $1 WHERE id = $2`, [cleaned, row.id])
        updated++
      }
    }
    console.log(`DB: updated ${updated} terminals`)
  } catch(e) {
    console.error('DB error:', e.message)
  } finally {
    await pool.end()
  }

  const BATCH = 100
  let cloudDeleted = 0
  for (let i = 0; i < publicIds.length; i += BATCH) {
    const batch = publicIds.slice(i, i + BATCH)
    try {
      const result = await new Promise((resolve, reject) => {
        const params = batch.map(id => `public_ids[]=${encodeURIComponent(id)}`).join('&')
        const options = {
          hostname: 'api.cloudinary.com',
          path: `/v1_1/${CLOUD_NAME}/resources/image/upload?${params}`,
          method: 'DELETE',
          headers: { 'Authorization': 'Basic ' + Buffer.from(`${API_KEY}:${API_SECRET}`).toString('base64') }
        }
        const req = https.request(options, (res) => {
          let data = ''
          res.on('data', chunk => data += chunk)
          res.on('end', () => { try { resolve(JSON.parse(data)) } catch { resolve({ raw: data }) } })
        })
        req.on('error', reject)
        req.end()
      })
      const deleted = result.deleted ? Object.keys(result.deleted).length : 0
      cloudDeleted += deleted
      console.log(`Batch ${i/BATCH+1}: ${deleted}/${batch.length} deleted`)
    } catch(e) {
      console.error(`Batch error:`, e.message)
    }
  }
  console.log(`\nDone. Cloudinary: ${cloudDeleted}/${publicIds.length}`)
}

main().catch(console.error)
